import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();

const sources = [
  { locale: 'el', docsDir: path.join(root, 'docs'), prefix: '' },
  { locale: 'en', docsDir: path.join(root, 'i18n', 'en', 'docusaurus-plugin-content-docs', 'current'), prefix: '/en' },
  { locale: 'es', docsDir: path.join(root, 'i18n', 'es', 'docusaurus-plugin-content-docs', 'current'), prefix: '/es' },
];

function listMarkdownFiles(dir) {
  if (!fs.existsSync(dir)) return [];

  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) return listMarkdownFiles(fullPath);
    if (entry.isFile() && /\.(md|mdx)$/i.test(entry.name)) return [fullPath];

    return [];
  });
}

function parseFrontmatter(raw) {
  const match = raw.match(/^---\s*\r?\n([\s\S]*?)\r?\n---\s*/);
  const frontmatter = {};

  if (!match) return { frontmatter, body: raw };

  for (const line of match[1].split(/\r?\n/)) {
    const index = line.indexOf(':');
    if (index === -1) continue;

    const key = line.slice(0, index).trim();
    const value = line.slice(index + 1).trim().replace(/^['"]|['"]$/g, '');

    if (key) frontmatter[key] = value;
  }

  return { frontmatter, body: raw.slice(match[0].length) };
}

function cleanMarkdown(value) {
  return String(value || '')
    .replace(/import\s+.*?;\s*/g, ' ')
    .replace(/export\s+.*?;\s*/g, ' ')
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/!\[[^\]]*]\([^)]+\)/g, ' ')
    .replace(/\[([^\]]+)]\([^)]+\)/g, '$1')
    .replace(/<[^>]+>/g, ' ')
    .replace(/[#>*_~|{}]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function titleFromPath(relativePath) {
  return String(relativePath)
    .replace(/\\/g, '/')
    .replace(/\.(md|mdx)$/i, '')
    .split('/')
    .pop()
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function normalizeUrl(parts) {
  const url = parts.filter(Boolean).join('/').replace(/\/+/g, '/');
  const withLeadingSlash = url.startsWith('/') ? url : `/${url}`;
  return withLeadingSlash.replace(/\/index$/, '') || '/';
}

function permalinkFor(filePath, docsDir, prefix, frontmatter) {
  const relativePath = path
    .relative(docsDir, filePath)
    .replace(/\\/g, '/')
    .replace(/\.(md|mdx)$/i, '');

  if (frontmatter.slug) {
    const slug = frontmatter.slug.startsWith('/') ? frontmatter.slug.slice(1) : frontmatter.slug;
    return normalizeUrl([prefix, slug]);
  }

  return normalizeUrl([prefix, relativePath]);
}

function categoryFromPath(filePath, docsDir) {
  const relativePath = path.relative(docsDir, filePath).replace(/\\/g, '/');
  const category = relativePath.includes('/') ? relativePath.split('/')[0] : 'getting-started';

  return category
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

const index = [];

for (const source of sources) {
  const files = listMarkdownFiles(source.docsDir);

  for (const filePath of files) {
    const raw = fs.readFileSync(filePath, 'utf8');
    const { frontmatter, body } = parseFrontmatter(raw);

    const relativePath = path.relative(source.docsDir, filePath).replace(/\\/g, '/');
    const title = frontmatter.title || titleFromPath(relativePath);
    const description = frontmatter.description || '';
    const content = cleanMarkdown(body);
    const excerpt = content.slice(0, 280);

    index.push({
      locale: source.locale,
      title,
      description,
      category: categoryFromPath(filePath, source.docsDir),
      permalink: permalinkFor(filePath, source.docsDir, source.prefix, frontmatter),
      excerpt,
      searchText: cleanMarkdown(`${title} ${description} ${content}`),
    });
  }
}

const greekItems = index.filter((item) => item.locale === 'el');

for (const locale of ['en', 'es']) {
  const hasLocalizedItems = index.some((item) => item.locale === locale);

  if (!hasLocalizedItems) {
    for (const item of greekItems) {
      index.push({
        ...item,
        locale,
        permalink: normalizeUrl([locale, item.permalink]),
      });
    }
  }
}

const deduped = Array.from(
  new Map(index.map((item) => [`${item.locale}:${item.permalink}`, item])).values(),
).sort((a, b) => `${a.locale}:${a.title}`.localeCompare(`${b.locale}:${b.title}`));

const outputPath = path.join(root, 'src', 'data', 'helpSearchIndex.js');

fs.mkdirSync(path.dirname(outputPath), { recursive: true });

fs.writeFileSync(
  outputPath,
  `// This file is generated by scripts/build-help-search-index.mjs\n` +
    `// Do not edit manually.\n\n` +
    `const helpSearchIndex = ${JSON.stringify(deduped, null, 2)};\n\n` +
    `export default helpSearchIndex;\n`,
  'utf8',
);

console.log(`Generated ${deduped.length} help search records.`);
